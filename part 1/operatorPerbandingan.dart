void main() {
  /**
   * berikut operator yang berlaku di dart
   * 
   * == : sama dengan
   * != : tidak sama dengan
   * >  : lebih dari 
   * < : kurang dari 
   * >= : lebih dari atau sama dengan
   * <= : kurang dari atau sama dengan
   */

  print(10 == 10);
  print(10 != 10);
  print(10 < 10);
  print(10 > 10);
  print(10 >= 10);
  print(10 <= 10);
}
