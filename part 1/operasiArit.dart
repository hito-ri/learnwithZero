void main() {
  /**
   * Berikut operator aritmatika pada dart :
   *  + : penjumlahan
   * - : pengurangan 
   * / : pembagian (hasil double)
   *  ~/ : pembagian (hasil int)
   * * : perkalian
   *  %: sisa bagi
   */

  var first = 10;
  var second = 3;

  print(first + second);
  print(first - second);
  print(first * second);
  print(first / second);
  print(first ~/ second);
  print(first % second);
}
